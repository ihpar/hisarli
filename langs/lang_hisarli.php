        "en" => "<p>Hisarlı Ahmet was born on 13 July 1908 in Kütahya Kale-i Bala (Yukarı Hisar) as the second son of Ayşe
  Hanım and Musta' (Mustafa). He spent his childhood and youth with his father, who was a kavaf (shoemaker).
  Although not noteworthy, Hisarlı states that they played war games with the youth of Karşı Tepe (Hıdırlık)
  by putting stones on the leather between two long ropes called “sapankaya”, throwing stones at each other;
  He used to happily talk about the news that his voice was heard from the surrounding villages when a folk
  song was sung in a place called Topbaşı.</p>
<p>He met the baglama that would change the course of his life and carry him to the world, thanks to a young
  man he knew in prison, where he entered to serve a three-day prison sentence because he did not or could
  not pay \"road tax\". As soon as he comes out, my grandfather breaks the baglama he bought with a lump of
  clay or a mintan [1]. He buys it again; my grandfather breaks it again. In the end, my grandfather gave
  up.
  He advances in the baglama. He was inspired by the masters of that period, Hüseyin Ağa of Dülger and Ethem
  Efendi of Circassians. The beauty of his voice, the breadth of his repertoire and his mastery in baglama
  are legendary. He bounds up with his family. He loves nature. He can be friends with people of all ages.
  On top of that, his friendliness and devotion make him a sought-after traveller for the weekly meeting of
  the youth.</p>
<p>These travellers, who have great benefits in raising, educating, and adapting to social life, have strict
  rules. Do not be late for the meeting. During a lecture, words are not interfered with. Silence prevails
  when the song begins to be sung. Since it is comfortable to sit on floor cushions and on places called
  cedars at a height of 15-20 cm, the feet are collected [2] immediately. Kneeling or sitting cross-legged.
  No
  smoking. When these rules are violated, the penalty deemed appropriate by the Gezikbaşı is applied. If the
  person is rich, two trays of baklava; If he is poor, he is asked to bring water from a distant fountain. I
  would like to tell an anecdote I heard from my father: One day, the meal begins. Since he is a lively
  person, he cannot stand it, my father throws a samsa (slice) out of baklava. What does he feel! Instead of
  taste, he has something awful in his mouth. Immediately, my father signs with his brows and eyes to the
  young man of the house on the pretext of bringing water from a distant fountain. Baklava is taken away.
  When the young man arrives, it is said to him, \"It was very nice, we did not leave it to you.\" At the end
  of the meeting, when the lady of the house saw the sherbet pot full, it was too late. He realizes that his
  wife is not aware of this incident either. This graceful gesture of the guests embarrassed him. He wants
  to repeat the invitation immediately to correct his mistake. There have been many other beautiful events
  similar to this in social relations. Young men's experiences, their knowledge about life, especially
  having done their military service, young girls growing up among girls, etc, all these are very important
  in terms of leaving positive impressions on the young people of travel.</p>
<p>Attending these meetings is almost a reference for young people. They are also complimented for playing
  the \"Friday song\". Then military service comes. As my father tucked his three strings under his arm, he
  goes to the military branch. When the commander who chose my father for the artillery class because he was
  a well-built young man, asked about the bulge under his arm, my father showed him the bag. “Then you are
  also a member of the band,” says the commander. Thus, Hisarlı learned the clarinet, which he would later
  play in the Kütahya Municipal Band and learned how to read musical notes while in the military.</p>
<p>The events of the soldier's return direct the course of my father's life. The death of my grandfather,
  the death of his Aga (brother) and his marriage with his aunt (his brother's wife, my mother) put the
  burden of the family on him. He continues his father's profession as a Kavaf. For a long time, my brother,
  the son of my deceased uncle, my sister from the same father (Hisarlı) and I, three siblings lived without
  ever separating from each other and feeling privileged. We owe this to my dear decedent mother and
  father's indiscriminate love and care.</p>
<p>While Kavafism was still going on, they gave a place to the residents of Hisar because a sanatorium would
  be built in the Hisar in 1944. In 1945-46 I started primary school. In the following years, my father
  starts to run a coffee house in the bazaar where there are coffee shops and kavaf shops called kavafhane
  or arasta. Naturally, the three strings is hanged on the wall. I just remembered: One day my coachman (who
  has a one-horse carriage) İbrahim (Abraham) came to the cafe. İbrahim started playing with my father by
  tuning it with the baglama he took out from under his arm. I still hear the ringing in my ears. You would
  feel yourself among the sounds of nightingales in the rose garden in an inexplicable harmony. In our
  baglamas, the upper string is tuned to an octave treble and the fret is in the middle of the neck, and
  there is an addition called “harmonic string”. After the other strings are tuned, the harmony string auger
  is done with the teeth. This is the trick of the job. The tea and coffee service stops. While everyone is
  listening in awe, as is always the case everywhere, someone who \"has not played a cüme debşeği\" [3] in
  that
  day’s words, makes a request. Hisarlı instantly ends the conversation and says, \"You don't look for a
  peşrev on drums and zurna [4], the luck of the draw,\" he shows the way to that impertinent, but the
  conversation also ends.</p>
<p>I know that bards such as Âşık Veysel and Âşık Davut Sulâri and other vocal and saz artists did not leave
  without stopping by my father when they came to Kütahya.</p>
<p>In 1940-42 and later, the Kütahya team was also invited to the program of Ankara Radio, which promoted
  the folklore of the provinces as \"Province to Province Anatolia\". Tailor Sadık (Turkish), Kambur Celal
  (Tavukçuoğlu), Nuri Çavuş, Fındık Hüseyin and my father, and also local dance team performed the local
  folk songs and dances. They received the admiration and appreciation of all artists, especially Muzaffer
  Sarısözen, the master commentator, teacher, and compiler of that period. The mastery of the Hisarlı's saz
  and the agility of his voice and glottis (hançere) did not slip past the attention of the teacher. He was
  offered to stay on the radio. However, my father gave a negative response to this offer, thinking that he
  was the only protector of the family.</p>
<p>While life goes on in Kütahya, my father teaches the curious young people who come to coffee, for a small
  fee, and those who fall out of their tune or pitch find themselves with my father. If someone says \"Ahmet
  emmi teach saz to me\", he marks the stem of the instrument with chalk, \"sol sol re re sol sol re (Tren
  Gelir Hoş Gelir)” and says \"Come on! The rest is after you learn this\".</p>
<p>Meanwhile, my relationship with my father was distant, as was my brother and sister. I mean, there was
  love and affection, but there was also a distance between us. The flame of the saz engulfed me in middle
  school. I couldn’t tell my father, but I went and told my mother. I said, \"My father teaches everyone, so
  he can teach me too.\" My decedent mother scolded me for complaining about my father playing the saz. “Are
  you going to be an instrumentalist too?” she said. However, I had a background, so I started playing
  something in baglama in a very short time. Cemal Bey (Kamacı), my late gymnastics teacher at school, heard
  about it. \"Teach me too!\" he commissioned me. One day, I hide my saz under my arm as if I am doing
  something shameful. Now I commemorate them both with mercy, my two very precious teachers, mathematician
  Recep Hodja, and my French teacher, Kasım Bey, saw me with my instrument. Both of them scolded, as my
  mother said, \"Are you going to be an instrumentalist?\" However, later on, I was fortunate to hear from
  both of them that they were proud of my being an artist. This is why I told you this: Nothing has changed
  in the half-century between my father and my youth. My primary school is over. He asked me because he did
  not or could not educate his other children. When we got a positive response, we went to the health center
  to get the health report required for school registration. The health officer on duty knows my father. He
  says: \"Ya Amet’a! What is the need to make your child read? Give it to a tailor or barber to learn the
  craft”. I remember my father's face. \"Mind your business. Give us our report,\" he said. \"Let Ahmet Mehmet
  Bey's [5] sons read but not Amet'a’s, huh? Get off!\" he scolded.</p>
<p>I started middle school. Those who come to the coffee shop and ask my father for saz, those who come for
  repairs, when the number of people who wanted to buy mandolin, flute, trumpet, etc. for schools increased,
  my father started to go to Konya to Istanbul to meet the need. When the shop was filled with musical
  instruments, the coffee house turned into a music shop where musical instruments were sold. The coffee
  shop was also oversized. Naturally, the number of saz students increased. Later on, we used to see my
  father as a teacher for many years in music events of associations such as Kızılay and Yeşilay. The
  incident that took place during my secondary education was repeated for higher education. \"Son, do you
  want to study?\" said my father. I know my father's income. When I hesitated a bit, he said, “Son, if you
  want to study, I will sell my jacket and have you read it again”. It was an expression of sacrifice and
  determination. By the way, I remember he quit smoking right away. Unfortunately, in our society, some
  unknowingly make judgments. Just like my dad used to drink. He once said that he used to drink raki in his
  youth, but after starting a family and since I was a child, I have not seen my father ever drinking
  alcohol. I even witnessed that once, when he noticed that the chocolate that was offered has liquor, he
  split it out appropriately.</p>
<p>Under his very serious appearance, he had a kind and playful demeanour. Even at a time when he could make
  money from his instrument, he did not use it as a means of earning. Our life passed in a harmonious way,
  like every family from Kütahya, in the context of relatives, friends and neighbours. Either a pot or a
  tin-plated dish was used in the dishes that were eaten on the floor tables. The food taken with a piece of
  bread sandwiched between two or three fingers would not mess the fingers. In fact, in dishes such as
  moussaka, sets of food were formed in front of each person. No one would reach out in front of anyone.
  Anatolian dishes are the places where etiquette and table manners are best practiced. Unfortunately, in
  some documentaries, Anatolian food is portrayed as a battlefield, and forced and contrived images are
  displayed.</p>
<p>My family showed the experience and maturity they acquired from the traditions and customs of their
  members, even when I chose the daughter of an Anatolian family living in a different place from their
  lives, in Istanbul, as my wife, and they adapted to the new life as much as possible.</p>
<p>A \"Kütahya Night\" was organized to cover the expenses of the dormitories for the young people studying in
  Ankara and Istanbul, to give scholarships to some students, and to keep the Kütahya compatriots in the
  home together. A local folklore team was called from Kütahya. Folk songs were sung, local games were
  played, and local food was served. Traditional customs were revived. Hisarlı is a must for these nights.
  While I was in high school, I was in the girls' institute and the folklore team of the high school once.
  In general, these teams would have my father, Aşık Ömer (Kocaoğlu), tailor Sadık (Turkish), Pepe Osman,
  Shoemaker Sadık and deblek (darbuka) Fındık Hüseyin.</p>
<p>My father was meticulous in the way folk songs were played and sung, angry that they were changed and
  corrupted under the name of interpretation, saying \"they take gold and silver out of their mouth\" Once he
  found out that I was playing the baglama, he would force me to play together. I'll never forget, I used to
  listen to the decedent Yılmaz İpek on the shortwave İzmir radio and try to play longas and sirtos [6] like
  him. My father used to force me to play to show my skills when his friends came. While I was trying my
  best and waiting for a good job (!) from them, I would hear the sweet reprimand from them, \"Son, let's
  play something properly and we’ll listen\", and I would be offended.</p>
<p>As I learned later, when I was in Istanbul, the decedent Nida Tüfekçi, Yücel Paşmakçı and Muzaffer Akgün
  found my father when they stopped by Kütahya on tour (1957-58). They were fascinated by his voice.
  However, the tape recorder they had did not allow them to record properly. They returned empty-handed.
  Later, I heard it from their own mouths.</p>
<p>During my education, while following the music activities in Istanbul, I participated in the folklore
  studies of student associations such as MTTB -MTTF. I continued the works and concerts of the Istanbul
  Municipality Conservatory Turkish Folk Music Executive Committee, which I passed the exam and directed by
  my very esteemed teacher Adnan Ataman, as a guest artist. On these occasions, it was my privilege to bring
  my father together with the values of this art community, who knew my father from afar. Sometimes I went
  to the house of the Tüfekçi family and sometimes to the Yücel Paşmakçı's and became an intermediary in
  transferring my father's repertoire to TRT. Although I did not know the notes, I felt more comfortable and
  peaceful because I was the mediator for the compilations and notation to be done by my teachers. </p>
<p>Even if some of our friends who did private work make it look like it was taken from my father it is not
  possible to forget these veterans; “Çömüdüm” from Terzi Sadık, “Tıpır Tıpır Yörürsün” from Asım Doğan,
  “Ahmet Bey’in Bir Küheylan Atı Var” from Ali Çavuş, and “Sepet Alıp Girmiş” from Hakkı Özevren.</p>
<p>There were some melody deficiencies or prosody errors in the writing of the notes, since the folk songs
  taken from my father could only be quatrains or couplets, not all, due to the quality of the tapes at that
  time and the scarcity of reel tapes. Although over time, these deficiencies and saz attitudes were
  resolved with one-on-one sightings, but unfortunately, the saz style of the Kütahya region could not be
  performed with care in some recordings.</p>
<p>From time to time I am asked, \"Did your father make these folk songs?\" that. I say, \"These folk songs are
  not my father's composition, but his \"interpretation.\" If these folk songs would have been taken from
  someone else… I do not want to go into too much detail, and I leave the decision of this to you with the
  examples I will tell below. We have two examples of folk songs. “Gar Mı Yağdı Kütahya'nın Dağına\" and
  \"Mustafa’m Kaşların Kara\". These two songs were borrowed from my father. When the notes are examined, the
  effect of Hisarlı's Kütahya plectrum style and the melodic structure of his folk songs and glottis
  technique can be seen.</p>
<p>Hisarlı is summoned to the Kütahya Municipal Band upon learning that he plays the clarinet. He accepts it
  as a contribution to the family economy. He used to make a variation on the anthem they played at the time
  I passed on public holidays and school walks. My hands and feet would wander for some reason. My father
  was a religious person. He was in his prayer. From time to time, he would go out to the minaret to call
  the prayer or give sala on Fridays or in the morning. He even prepared to be a muezzin, took the exam and
  was successful. However, they said, \"You will leave the instrument Amet’a.\". “Mind your business. I am
  closer to Allah than you are with my saz”, he told with sadness, resentment and anger that he slammed the
  door. In the meantime, he went to Hejaz, became a pilgrim, the same warnings from the right and left, but
  he had found the way of worship and being human.</p>
<p>Here's what comes to mind. What did his fellow countrymen do to this person who served the Kütahya
  culture so much? On March 17, 1997, I received a plaque given by the Kütahya Chamber of Commerce and
  Industry to those who passed the outstanding service to Kütahya, on behalf of Hisarlı Ahmet, as his son,
  from the Speaker of the Grand National Assembly of Turkey. This is my source of pride. Also, the street
  where his shop is located was given his name by showing a good example of appreciation by the people of
  Kütahya. His bust was put in the cultural park, but now both are gone. His name is not on the street, and
  there is a fountain at the place of his bust. It’s okay. People of Kütahya should not forget this. For as
  long as I have known myself, if you don't count my childhood, Kütahya's name has been mentioned with
  Hisarlı for over sixty years. By saying \"A Kütahya folk song taken from Hisarlı Ahmet\" or \"This Kütahya
  folk song was taken from Hisarlı Ahmet\".</p>
<p>Although I find it very exaggerated, they say that one of the conservatory teachers said to his students,
  \"You may not know the National Anthem by heart, but I will not forgive you if you do not know Hisarlı
  Ahmet\". Looking at all these and relying on my service to this culture for more than half a century and
  your great tolerance, I would like to say that Hisarlı Ahmet is the \"summary\" of Kütahya.</p>
<p>I am in this art community. Both the artists on the radio, the conservatory students and those who know
  the quality keep Hisarlı in their hearts. I can see and feel it. This is enough for me.</p>
<p>You can come across the surname “İnegöllüoğlu” in folk songs in the TRT repertoire. My grandfather, who
  was a Kavaf (shoemaker), used to go to İnegöl from time to time for the sale of Yemeni (flat-heeled
  shoes). He was also known as Eynigöllü among his friends. When the surname law was enacted, the population
  clerk recorded it in the register. When I stopped by the PTT to receive the pocket money my father sent me
  during my education, I was having problems with İnegöllüoğlu on my identity card because of the different
  spellings in the posts. When I told this to my father, we decided as a family and took Hisarlı, which has
  now become my father's nickname, as his surname with a court decision.</p>
<p>As I continue my education, when my father came to buy musical instruments and to see me, we were
  together with the artists in the Istanbul Municipal Conservatory Turkish Folk Music Group and with the
  radio artists. This pleased both my father and the artists, and both parties were feeling wonderful.
  Bringing together my father and the decedent Ahmet Gazi Ayhan created a flood of emotions. The meeting of
  two masters who admired each other so much without seeing each other was indeed worth seeing. It was as if
  they were both flying in the sky. He was saying “Play Ahmet Ağa and let me listen to you” the other was
  saying “no, I want to listen to you live”.</p>
<p>My father's favorites were Neriman Altındağ, Nida Tüfekçi, Adnan Ataman, Yücel Paşmakçı, Seha Okuş, Kemal
  Koldaş, Şahin Gültekin, Tuncer İnan, Hamdi Özbay, Güner Karabacak, and my friends whose names I can't
  name, from 66 period. Because these artists were performing the folk songs taken from my father in the
  most appropriate way to their originality and attitude, and this made my father very happy. It is the
  common view of the art community that Kütahya, which experienced the Seljuks, the Germiyan Principality
  and the Ottoman Empire, has a high level of artistic structure with the richness of its folklore and
  lifestyle. The delicacy and elegance of the tile work with needlework, embroidery and thread embroidery in
  handicrafts also manifest itself in folk songs. The agility, breadth, rhythm differences of the melodic
  structure and the clarity of the emotional expressions in the lyrics are also seen in folk dances. In folk
  songs with the same play, while you feel the toughness and bravery in men, you see the grace and naivety
  in women. In henna folk songs, it is aimed to make brides and mothers of the girls cry. Just like in the
  folk songs \"Altıntas İçinde Kınamı Ezdiler\" and \"Gül Kuruttum\".</p>
<p>Apart from the days when young people such as gezeks and maidens gather and have fun, in ceremonies such
  as henna nights, hamamönü, gakmık, groom release (güvey salma), dowry, \"Gar mı Yağdı\", \"Ahmet Bey\",
  \"Mustafa’m Kaşların Kara\", \"Meşeden Gel\", \"Portakalım Çaya Düştü\", \"Bedestene Vardım Şalvar İsterim\",
  \"Fincanın Dibi Noktalı\" and \"Aya Bak Yıldıza Bak\"; In addition, the dances of the folk songs \"Hisardan
  İnmem Diyor\", \"Çömüdüm\", \"İğnem Düştü Yerlere\" and especially \"Yasemin Dalını Yar Neden Eğmeli\" folk songs
  are performed by the ladies. Other folk songs are also songs of harmony, and they are definitely not
  accompanied with dance. Unfortunately, as today's young people do, \"Elif\", \"Ferace\", \"Kütahya’nın
  Pınarları (Kütahya’s Springs)\" etc. are the songs of harmony and lament. Definitely should not be
  accompanied with dance. Especially our games are played with a kırık zeybek (broke zeibek) and a welcome
  type, it is played with spoons [7]. One person plays or two people play facing each other. It is not a
  group
  dance. </p>
<p>Some young people may not like it, but folk songs like Aydın Havası (Aydın Air) or Zeybek Havası (Zeibek
  Air) are not played with the hands and feet in the air. No one should have the right to say \"My
  interpretation, I did it\". If they do, the play figures of Anatolia, which are so rich and each expressing
  their own region, are reduced to a few simple figures.</p>
<p>It saddens me to see that even young people with conservatory education change the words in some folk
  songs, ignoring the meaning of their content. They use the word \"Ah\", which has a different meaning [8]
  (bewailment), instead of the expression \"A\" in \"A İstanbul Sen Bir Han Mısın\", and they use the expression
  of the phrase “Git Güzel Karşımda(n) Ağlayıp (Go away Beautiful and Cry)” to banish, instead of using the
  expression of begging “Git Güzel Karşımda Ağlayıp Durma (Go away beautiful, please don’t cry in front of
  me)”.</p>
<p>Instead of \" Fincanın Dibi Noktalı Gelinler Kızdan Okkalı\", they use the word \"charming\", which has a
  different meaning; instead of the Anatolian idiom describing \"mother-in-law\" in \"Kocakarıları Merdivenden
  Atmalı\", the adjective is used as \"we must throw grumpy women off the stairs\", which can be considered an
  insult; and in \"Uçkuru Bezden Yaparlar, Gelini Kızdan Yaparlar” they change it into \" they make the urba
  (robe) out of cloth”. However, the urba-robe is not made of cloth. Urba is either chintz, silk, or velvet.
  It is not possible to understand why the female artist who sings the folk song \"Elif Dedim Be Dedim Kız
  Ben Sana Ne Dedim\" uses the word \"yar (dear) [9]\" instead of \"girl\" to serve what reason.</p>
<p>In the centre, three-stringed baglama (I guess there is not much nowadays), tambura, deblek, def, spoon,
  zilli maşa [10]; in open areas, drums, zurna, shepherd's whistle and string instrument violin can be seen
  from
  time to time. Folk songs are generally up-and-down (such as \"Çatal Çambaşına Koyudum Keseri\", \"Havada
  Durna Sesi Gelir\") descending (such as \"Feracemin Ucu\", \"Mustafa’m Kaşların Kara\", \"Yasemin Dalı\") or
  ascending (such as \"Gidin Bulutlar Gidin\", \"Eremedim Vefasına Dünyanın”). Folk songs are also rich in
  terms of style. The 9/8 (2+2+2+3) of the 9-time procedures is common. Some of the 9/4, 7/4, 4/4, 2/4 and
  mixed styles were used. I think “Yasemin Dalı” in 27/8 (12/8+15/8) style is the only example in folk
  music. Decision voices are usually \"La\". There are examples of folk songs ending in \"sol\" and \"fa sharp\".
  Despite his left-wing determination, Hisarlı would persistently play \"Ben Kendimi Gülün Dibinde Buldum \"
  in \"fa\", that is, in the müstezat order. There is no wordless play or melody as far as I remember. Hisarlı
  archive CD has two free airs, \"Kuzu\" and \"Leyla'm Zülüflerin\", which my father also read. </p>
<p>I would like to briefly talk about a CD. I talked about how folk songs are transferred to TRT. All of the
  recordings on this CD were made at my request and insistence, even when my father came to us in Istanbul
  when he was tired or depressed. My wife, Ms. Tuncay, is also a witness to these.</p>
<p>Another event that I could not pass without recording was the meeting of Hasan Saltık, producer of Kalan
  Music. I skip the conversations. He reviewed my father's recordings, which I recorded on my
  semi-professional tape recorder at home and transferred them to CD. Thus, Hisarlı Ahmet joined the
  community of those who keep our culture alive. I express my gratitude and thanks to him for this
  service.</p>
<p>It was my grandson, İsmail Pektaş, that I, along with his grandson, İsmail Pektaş, had the folk songs
  that my father desired to but could not fulfil in his lifetime. With the support of respected businessman
  Nafi Güral, this book, which was printed in a thousand copies, was distributed free of charge to the
  entire art community and folk song lovers.</p>
<p>We built two Hisarlı Ahmet rooms in the Kütahya State Hospital and the Social Security Hospital from the
  income of the limited tickets I transferred to a friend from a very nice commemoration night held at the
  Cemal Reşit Rey Concert Hall. Hisarlı Ahmet and Kütahya folk songs are among the preferred subjects in the
  conservatory graduate studies. In addition, many commemorative programs were held on TRT's TV and radio
  channels. All this is a source of pride for me.</p>
<p>There may be some things I forgot. As the son of Hisarlı Ahmet and being the closest person to him with
  my artistic identity, I tried to explain what I knew and what I could remember as best I could.</p>
<p>The presence and work of you, art lovers, even those of Hisarlı Ahmet, is a source of honour and pride
  for us, the Hisarlı family. I would like to thank you, my young friend, Uğur Türkmen, who prepared this
  symposium, and those who contributed. BEST REGARDS…</p>
<p>MUSTAFA HİSARLI - Master Architect, TRT Artist</p>
